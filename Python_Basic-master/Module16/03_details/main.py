shop = [['каретка', 1200], ['шатун', 1000], ['седло', 300],
        ['педаль', 100], ['седло', 1500], ['рама', 12000],
        ['обод', 2000], ['шатун', 200], ['седло', 2700]]

name_item = input('Название детали: ')
count_item = 0
summ = 0
for i in shop:
    if i[0] == name_item:
        count_item += 1
        summ += i[1]

print('Кол-во деталей —', count_item)
print('Общая стоимость —', summ)